package br.com.empiricus.exercicios29062022;

public class IncrementoDecremento {

	public static void main(String[] args) {
		int i = 1, x = 3, y = 5, z = 7;

		System.out.println(i++);
		System.out.println(i);

		System.out.println(++x);
		System.out.println(x);

		System.out.println(y--);
		System.out.println(y);

		System.out.println(--z);
		System.out.println(z);

	}

}
