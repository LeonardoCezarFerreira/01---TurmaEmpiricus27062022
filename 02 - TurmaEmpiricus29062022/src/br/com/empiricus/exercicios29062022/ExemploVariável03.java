package br.com.empiricus.exercicios29062022;

public class ExemploVariável03 {

	public static void main(String[] args) {
		int var1 = 10;
		double var2 = 10.0;

		System.out.println("Valor da 1ª variável: " + var1);
		System.out.println("Valor da 2ª variável: " + var2);
		System.out.println();

		var1 = var1 / 4;
		var2 = var2 / 4;

		System.out.println("Valor da 1ª variável: " + var1);
		System.out.println("Valor da 2ª variável: " + var2);
	}

}
